# Pounds to kilogram converter




# Ask user for weight in pounds

pounds = int(input("Enter the weight in pounds: "))


# Convert Pounds to kilograms

kilograms = pounds / 2.2046


#Display the result

print (pounds, "pounds are converted to", format(kilograms,'.2f'), "kilograms")
    
